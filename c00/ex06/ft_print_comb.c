#include <unistd.h>

void   ft_print_comb(void){

        char n100; 
        char n10;
        char n1;
        char comma;
        char space;

        n100 = '0';
        n10 = '0';
        n1 = '0';
        comma = ',';
        space = ' ';
        
        while(n100<='9')
        {
                while(n10<='9')
                {
                        while(n1<='9')
                        {
                                if((n1 == n10) || (n1 == n100) || (n10 == n100))
                                {
                                n1++;
                                }
                                else 
                                {
                                       write(1, &n100, 1);
                                       write(1, &n10, 1);
                                       write(1, &n1, 1);
                                       write(1, &comma, 1);
                                       write(1, &space, 1);
                                       n1++;
                                }
                        }
                        n1='0';
                        n10++;

                }
                n10='0';
                n100++;
        }
}



