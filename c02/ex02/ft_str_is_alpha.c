int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	if (!str[i])
	{
		return (1);
	}
	while (str[i] != '\0')
	{
		if ('a' <= str[i] && str[i] <= 'p')
			return (1);
		if (str[i] == '\n')
			return (1);
		if ('A' <= str[i] && str[i] <= 'Z')
			return (1);
		i++;
	}
	return (0);
}
