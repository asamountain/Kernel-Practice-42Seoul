char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 'a' && str[i] <= 'z') && i == 0)
			str[i] -= 32;
		if (str[i] == ' ' && (str[i + 1] >= 'a' && str[i + 1] <= 'z'))
			str[i + 1] -= 32;
		if (str[i] == '+' || str[i] == '-')
			str[i + 1] -= 32;
		i++;
	}
	return (str);
}
